    private void showDataTable(List<DongSP> list) {
        dtm.setRowCount(0);
        for (DongSP dsp : list) {
            dtm.addRow(dsp.toDataRow());
        }
    }

    private void fillData(int index) {
        DongSP dsp = listDSP.get(index);
        lblID.setText(dsp.getIdDSP());
        txtMa.setText(dsp.getMaDSP());
        txtTen.setText(dsp.getTenDSP());
    }

