    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        String maDSP = txtMa.getText();
        String tenDSP = txtTen.getText();
        DongSP dsp = new DongSP(maDSP, tenDSP);
        JOptionPane.showMessageDialog(this, dspsi.add(dsp));
        listDSP = dspsi.getAll();
        showDataTable(listDSP);
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int row = tbDongSP.getSelectedRow();
        String idDSP = listDSP.get(row).getIdDSP();
        String maDSP = txtMa.getText();
        String tenDSP = txtTen.getText();
        DongSP dsp = new DongSP(maDSP, tenDSP);
        JOptionPane.showMessageDialog(this, dspsi.update(dsp, idDSP));
        listDSP = dspsi.getAll();
        showDataTable(listDSP);
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int row = tbDongSP.getSelectedRow();
        String idDSP = listDSP.get(row).getIdDSP();
        JOptionPane.showMessageDialog(this, dspsi.delete(idDSP));
        listDSP = dspsi.getAll();
        showDataTable(listDSP);
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tbDongSPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbDongSPMouseClicked
        // TODO add your handling code here:
        int row = tbDongSP.getSelectedRow();
        fillData(row);
    }//GEN-LAST:event_tbDongSPMouseClicked

